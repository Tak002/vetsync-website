/**
 * Build-time script: reads docs/help/ markdown files and generates
 * src/generated/help-data.ts so the app never needs runtime fs access.
 */
import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const HELP_DIR = path.join(__dirname, "..", "docs", "help");
const OUT_FILE = path.join(__dirname, "..", "src", "generated", "help-data.ts");

const CATEGORY_META = {
  "getting-started": { order: 1, icon: "Rocket" },
  chart: { order: 2, icon: "ClipboardList" },
  tasks: { order: 3, icon: "ListChecks" },
  calculator: { order: 4, icon: "Calculator" },
  patient: { order: 5, icon: "PawPrint" },
  settings: { order: 6, icon: "Settings" },
  mobile: { order: 7, icon: "Smartphone" },
  board: { order: 8, icon: "MessageSquare" },
};

function extractTitle(content) {
  const match = content.match(/^#\s+(.+)$/m);
  return match ? match[1] : "Untitled";
}

function extractDescription(content) {
  const lines = content.split("\n");
  let foundTitle = false;
  for (const line of lines) {
    if (line.startsWith("# ")) {
      foundTitle = true;
      continue;
    }
    if (foundTitle && line.trim() !== "" && !line.startsWith("---")) {
      return line.trim();
    }
  }
  return "";
}

function extractHeadings(content) {
  const headingRegex = /^(#{2,3})\s+(.+)$/gm;
  const headings = [];
  let match;
  while ((match = headingRegex.exec(content)) !== null) {
    const level = match[1].length;
    const text = match[2];
    const id = text
      .toLowerCase()
      .replace(/[^a-z0-9가-힣\s-]/g, "")
      .replace(/\s+/g, "-");
    headings.push({ id, text, level });
  }
  return headings;
}

function extractRelatedGuides(content, currentCategory) {
  const linkRegex =
    /\[([^\]]+)\]\((?:\.\.\/([^/]+)\/([^)]+)\.md|\.\/([^)]+)\.md)\)/g;
  const guides = [];
  let match;
  while ((match = linkRegex.exec(content)) !== null) {
    const title = match[1];
    if (match[2] && match[3]) {
      guides.push({ category: match[2], slug: match[3], title });
    } else if (match[4]) {
      guides.push({ category: currentCategory, slug: match[4], title });
    }
  }
  return guides;
}

// Build all data
const categories = [];
const guides = [];

for (const [id, meta] of Object.entries(CATEGORY_META)) {
  const categoryDir = path.join(HELP_DIR, id);
  if (!fs.existsSync(categoryDir)) continue;

  const files = fs.readdirSync(categoryDir).filter((f) => f.endsWith(".md"));
  const categoryGuides = [];

  for (const file of files) {
    const slug = file.replace(".md", "");
    const content = fs.readFileSync(path.join(categoryDir, file), "utf-8");
    const title = extractTitle(content);
    const description = extractDescription(content);

    categoryGuides.push({ slug, title, description });
    guides.push({
      slug,
      category: id,
      title,
      description,
      content,
      headings: extractHeadings(content),
      relatedGuides: extractRelatedGuides(content, id),
    });
  }

  categories.push({
    id,
    order: meta.order,
    icon: meta.icon,
    guideCount: categoryGuides.length,
    guides: categoryGuides,
  });
}

categories.sort((a, b) => a.order - b.order);

// Write output
const outDir = path.dirname(OUT_FILE);
if (!fs.existsSync(outDir)) {
  fs.mkdirSync(outDir, { recursive: true });
}

const output = `// Auto-generated by scripts/generate-help-data.mjs — DO NOT EDIT
/* eslint-disable */

export const HELP_CATEGORIES = ${JSON.stringify(categories, null, 2)} as const;

export const HELP_GUIDES = ${JSON.stringify(guides, null, 2)} as const;
`;

fs.writeFileSync(OUT_FILE, output, "utf-8");
console.log(
  `Generated help data: ${categories.length} categories, ${guides.length} guides → ${OUT_FILE}`
);
